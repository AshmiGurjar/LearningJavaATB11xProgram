package ex_02_Java_Literals;

public class Lab012_NonPrimitive_Literals {
    public static void main(String[] args) {

        // Primitive Data type (Defined by Java Guys)
        // Max,Min, Size Defined
        byte b = 15;
        int age= 55;


        //Non primitive Data type (Defined by user),Reference DT
        //No size,No Max_Min Value
        //Byte=8,Bits -64
        //String,Array,Classes,Enum
        String name= "Ashmi";
        int[] arrays_of_items = new int[10];

    }
}
