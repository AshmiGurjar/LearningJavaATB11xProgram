package ex_01_Java_Basic;

public class Lab006_Println_vs_Print {
    public static void main(String[] args) {
//        Ln = New line
        System.out.println("Hello World");  //Print in new line
        System.out.println("This is Println statement"); //Print in new line
        System.out.print("How are you?"); //Print in same line
        System.out.print("This is Print statement"); //Print in sane line
    }
}
