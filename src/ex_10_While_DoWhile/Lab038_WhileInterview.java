package ex_10_While_DoWhile;

public class Lab038_WhileInterview {
    public static void main(String[] args) {

        //Ex_1
        int i=0;
        while(i<0){
            System.out.println("Print Data");
            System.out.println(i);
            i++;
        }

        //Ex_2
        int i1=10;
        while (i1>=1){
            System.out.println(i1);
            i1--;
        }
    }
}
